def create_generator(opt):
    # Initialize the network
    generator = getattr(network, opt.network)()
    if opt.load_name:
        # Load a pre-trained network
        pretrained_net = torch.load(opt.load_name)
        load_dict(generator, pretrained_net)
    print('Generator is created!')
    return generator

def create_discriminator(opt):
    # Initialize the network
    discriminator = network.Discriminator()
    # Init the network
    network.weights_init(discriminator, init_type = opt.init_type, init_gain = opt.init_gain)
    print('Discriminator is created!')
    return discriminator

def create_perceptualnet(opt):
    perceptualnet = PerceptualNet()
    # Pre-trained VGG-16
    vgg16 = torch.load(opt.vgg_name)
    load_dict(perceptualnet, vgg16)
    # It does not gradient
    for param in perceptualnet.parameters():
        param.requires_grad = False
    print('Perceptual network is created!')
    return perceptualnet

def create_perceptualnet_multilayer(opt):
    perceptualnet = PerceptualNet_multilayer(opt)
    # It does not gradient
    for param in perceptualnet.parameters():
        param.requires_grad = False
    print('Perceptual_multilayer network is created!')
    return perceptualnet

def load_dict(process_net, pretrained_net):
    # Get the dict from pre-trained network
    pretrained_dict = pretrained_net
    # Get the dict from processing network
    process_dict = process_net.state_dict()
    # Delete the extra keys of pretrained_dict that do not belong to process_dict
    pretrained_dict = {k: v for k, v in pretrained_dict.items() if k in process_dict}
    # Update process_dict using pretrained_dict
    process_dict.update(pretrained_dict)
    # Load the updated dict to processing network
    process_net.load_state_dict(process_dict)
    return process_net

